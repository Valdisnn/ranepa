// Напишите программу, которая сформирует следующую строку: "-x-xx-xxx-xx-x-"

public class NineTwoOne {
    public static void main(String[] args) {
        // Создаем пустую строку для формирования итоговой строки
        String result = "";

        // Задаем переменную для количества повторений каждого символа
        int repetitions = 1;

        // Используем цикл для создания строки
        for (int i = 0; i < 4; i++) {
            // Добавляем "-" в строку
            result += "-";

            // Используем еще один цикл для добавления "x" с заданным количеством повторений
            for (int j = 0; j < repetitions; j++) {
                result += "x";
            }

            // Увеличиваем количество повторений для следующего символа "x"
            repetitions++;

            // Добавляем "-" в конец каждой итерации
            result += "-";
        }

        // Выводим результат
        System.out.println(result);
    }
}