package FirstTask;// Дан текстовый файл (текст). Получите строку, состоящую из первых букв каждой строки файла. Найденную строку запишите в новый файл (переменную).

public class NineFiveFour {
    public static void main(String[] args){
        // Задаем исходный текст
        String text = "Представь,\nчто ты профессионал с обширным опытом в теме:\nдан некий текст. получите строку, состоящую из первых букв каждой строки текста. найденную строку запишите в новую переменную и выведите её.";

        // Разбиваем текст на строки
        String[] lines = text.split("\n");

        // Создаем пустую строку для записи первых букв
        String result = "";

        // Проходим по каждой строке и добавляем первую букву к результату
        for (String line : lines) {
            // Отбрасываем лишние пробелы в начале строки
            line = line.trim();

            // Если строка не пустая, добавляем ее первую букву к результату
            if (!line.isEmpty()) {
                result += line.charAt(0);
            }
        }

        // Выводим строки
        System.out.println(text + "\n");
        System.out.println(result);
    }
}